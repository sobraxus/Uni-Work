def getDegreeClass(mark):
    if mark >= 70:
        return "1st"
    elif mark >= 60:
        return "2i"
    elif mark >= 50:
        return "2ii"
    elif mark >= 40:
        return "3rd"
    else:
        return "Fail"
print("Your degree is", getDegreeClass(61))
